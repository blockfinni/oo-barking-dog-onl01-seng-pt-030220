class Dog 
  
  
  def name=(dogs_name)
    
    #instance variable to hold dogs name to be used 
    @name_of_dog = dogs_name
  end
  
  
  def name
    #instance variable to get name 
    @name_of_dog
  end
  
  
  def bark=(dog_bark)
    
    #instance variable 
    @dog_barks = dog_bark
  end
  
  def bark
    #instance variable to get bark
    @dog_barks
  end 
  
  
end#end class